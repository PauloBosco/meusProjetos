    private void preencheCliente(Cliente c) throws SQLException, ClassNotFoundException {
        DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
         while (dft.getRowCount() > 0) {
            dft.removeRow(0);
        }
         ImpressoesDAO imp = new ImpressoesDAO();
         
        DecimalFormat decimalFormat = new DecimalFormat("#0.00");
        String[] linha = {c.getNome(), c.getLogin(), decimalFormat.format(Cliente.getValorTotal(c)), imp.quantidadePorLogin(c.getLogin()) + ""};

        dft.addRow(linha);
        
    }
    
    
