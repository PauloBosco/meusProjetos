    private void preencheCombo(ArrayList<Computador> lista) {
   
        DefaultComboBoxModel dft = (DefaultComboBoxModel) jComboBox1.getModel();
         dft.removeAllElements();
         for(Computador c :lista){
            dft.addElement(c.getId());
         }
    }
    
    private void preencheTabela(ArrayList<Cliente> lista) throws SQLException, ClassNotFoundException {
        DefaultTableModel dft = (DefaultTableModel) jTable1.getModel();
         while (dft.getRowCount() > 0) {
            dft.removeRow(0);
        }
         
         for(Cliente c :lista){
             String[] linha = {c.getNome(), c.getLogin()};
            dft.addRow(linha);
         }
    }
    
    
