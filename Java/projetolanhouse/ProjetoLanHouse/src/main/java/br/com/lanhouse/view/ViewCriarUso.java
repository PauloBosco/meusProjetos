    private void jBtnCriarUsoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCriarUsoActionPerformed
        int selectedRow = jTable1.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String nome = (String) model.getValueAt(selectedRow, 0);
        String login = (String) model.getValueAt(selectedRow, 1);
           
        if (jTable1.getSelectedRow() < 0){
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }else{
            Cliente c = null;
            
            try {
                ArrayList<Cliente> clientes = new ClienteDAO().buscaPorLogin(login);
                if (!clientes.isEmpty()) {
                    // Considerando que vocÃª queira obter apenas o primeiro cliente retornado
                    c = clientes.get(0);
                      
                }
            } catch (SQLException ex) {
                Logger.getLogger(ViewCriarUso.class.getName()).log(Level.SEVERE, null, ex);
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(ViewCriarUso.class.getName()).log(Level.SEVERE, null, ex);
            }

            if (c != null) {
                // FaÃ§a algo com o objeto Cliente retornado, por exemplo:
                System.out.println("Cliente encontrado: " + c.getNome());
                Uso uso = new Uso();
                uso.setIdUsuario(c);
                
                int idComputadorSelecionado = (int) jComboBox1.getSelectedItem();
                
                ComputadorDAO computadorDAO = new ComputadorDAO();
                Computador computadorSelecionado = null;
                try {
                    computadorSelecionado = computadorDAO.buscar(idComputadorSelecionado);
                } catch (SQLException ex) {
                    Logger.getLogger(ViewCriarUso.class.getName()).log(Level.SEVERE, null, ex);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(ViewCriarUso.class.getName()).log(Level.SEVERE, null, ex);
                }
                 
               
                 if (computadorSelecionado != null) {
                uso.setComputador(computadorSelecionado);
                uso.setValorHora(Double.parseDouble(jTextValorHora.getText()));
                uso.setQtidadeMinutosTempo(60);
                uso.setServico(null);
                uso.setDiaLogin(LocalDateTime.now());

                // Chame o mÃ©todo inserir() do UsoDAO para salvar o novo uso
                UsoDAO usoDAO = new UsoDAO();
                try {
                    usoDAO.inserir(uso);
                    System.out.println("Novo uso criado com sucesso!");
                } catch (SQLException | ClassNotFoundException ex) {
                    Logger.getLogger(ViewCriarUso.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                // Computador nÃ£o encontrado
                System.out.println("Computador nÃ£o encontrado.");
            }
        } else {
            // Cliente nÃ£o encontrado
            System.out.println("Cliente nÃ£o encontrado.");
        }
            
        }
        
        
    }//GEN-LAST:event_jBtnCriarUsoActionPerformed

